    private void tryPlayServer_view() {
        
        String port = this.jTextField_Puerto.getText();
        String ip_bd = this.jTextField_IPBD.getText();
        
        if(!controladorDeInterfaz.notEmptyPort(port)){
            this.jTextArea_debug.append("You might have not been inicialized Port Param\n");
        }else if (!controladorDeInterfaz.lessThan6NumbersPort(port)){
            this.jTextArea_debug.append("It's Imposible to use this Port. Size shoud be under 6 digits\n");
        }else if(!controladorDeInterfaz.portOppen(port)){
            this.jTextArea_debug.append("This port has already been openned\n");
        }else if(!controladorDeInterfaz.tryPlayServer_controller(port, ip_bd)){
            this.jTextArea_debug.append("Something wrong has just happened with this Port. Try other Port\n");
        }else{
            
            this.jTextArea_debug.append("Server is Running right now\n");
            this.jComboBoxPorts.addItem(port);

        }
                
    }
