    private void jButton_StopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_StopActionPerformed
        
        String port = this.jTextField_Puerto.getText();
        
        if(!controladorDeInterfaz.tryStopServer_controller(port)){
            this.jTextArea_debug.append("Something wrong has just happened when you were trying to stop the server\n");
        }else{
            this.jTextArea_debug.append("Server has successfully stopped\n");
            
            
            if(controladorDeInterfaz.portSearch_Controller(port) != -1){
                this.jComboBoxPorts.remove(controladorDeInterfaz.portSearch_Controller(port));
                this.controladorDeInterfaz.eliminatePort_Controller(port);
            }
            

        }
        
        //System.out.println("\n\n");
    }//GEN-LAST:event_jButton_StopActionPerformed

